 public class FormatableFlags {
 
            
     public static void main(String[] args) {
        // Arial Flag
        String arialFlag = "*Arial Flag*";

        // Futura Flag
        String futuraFlag = "Futura Flag";

        // Helvetica Flag
        String helveticaFlag = "[Helvetica Flag](https://www.example.com)";

        // Gotham Flag
        String gothamFlag = "> Gotham Flag";

        System.out.println(arialFlag);
        System.out.println(futuraFlag);
        System.out.println(helveticaFlag);
        System.out.println(gothamFlag);
    }
}
